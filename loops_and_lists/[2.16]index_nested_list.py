# Defining a nested list with 3 sublists.
q = [['a', 'b', 'c'], ['d', 'e', 'f'], ['g', 'h']]

# Accessing and printing the first element of the first sublist.
print(q[0][0])  # Output: a

# Accessing and printing the second sublist as a whole.
print(q[1])  # Output: ['d', 'e', 'f']

# Accessing and printing the second element of the third sublist.
print(q[2][1])  # Output: h

# Accessing and printing the first element of the second sublist.
print(q[1][0])  # Output: d
